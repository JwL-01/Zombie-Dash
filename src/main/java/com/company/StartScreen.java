    public void paintStartScreen(Graphics g, int tileRow, int tileCol){
        Sprite grass = new Sprite ("/GRASS.png");
        int index = 0;
        for (int x = 0; x < tileRow; x++){
            for (int y = 0; y < tileCol; y++) {
                g.drawImage(grass.getSprite(), y * 32, x * 32, null);  // draw sprite at row,col of the jFrame
                index++;
            }
        }
        Sprite title = new Sprite("/GAMETITLE.png");
        g.drawImage(title.getSprite(), tileRow*tileRow/4, tileCol*tileCol/4, null);
    }

